#include "IRenderable.h"
#include "Graphics/GfxMgr.h"
#include "Resources/ResourceMgr.h"

//////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////

IRenderable::IRenderable() { GfxMgr->mRenderComps.push_back(this); }

//////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////

IRenderable::IRenderable(RenderPass* renderPass)
{
    if (renderPass)
        renderPass->AddRenderable(this);
    else
    {
        PrintDebug("Renderable created with null RenderPass.");
        GfxMgr->mRenderComps.push_back(this);
    }
}

//////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////

IRenderable::~IRenderable() { std::erase(GfxMgr->mRenderComps, this); }

//////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////

Resource<Shader>* IRenderable::SetShader(const std::string& path)
{
    mShader = ResourceMgr->Load<Shader>(path);
    return mShader;
}

//////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////

Resource<Shader>* IRenderable::SetShader(Resource<Shader>* shader)
{
    mShader = shader;
    return mShader;
}
